package org.wecancodeit.columbus.plantplanner;

public class PlantDouble {

	private String zone;

	public String getZone() {
		return zone;
	}

	public String getName() {
		return name;
	}

	private String name;

	public PlantDouble(String zone, String name) {
		this.zone = zone;
		this.name = name;

	}

}
